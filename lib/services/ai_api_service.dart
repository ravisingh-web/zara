  // ========== 3. VOICE TTS (GEMINI/GOOGLE CLOUD) ==========
  
  /// Convert text to speech using Google Cloud Text-to-Speech API
  /// 
  /// [text]: Text to synthesize
  /// [voice]: Optional voice name (defaults to ApiKeys.voiceName)
  /// [languageCode]: Optional language code (defaults to ApiKeys.languageCode)
  /// 
  /// Returns: Path to saved MP3 file, or null on error
  Future<String?> textToSpeech({
    required String text,
    String? voice,
    String? languageCode,
  }) async {
    try {
      final apiKey = ApiKeys.gemini;
      
      if (!_isValidKey(apiKey)) {
        if (kDebugMode) debugPrint('⚠️ Gemini API key not configured for TTS');
        return null;
      }

      final voiceName = voice ?? ApiKeys.voiceName;
      final langCode = languageCode ?? ApiKeys.languageCode;

      // Make request to Google Cloud TTS API
      final response = await http.post(
        Uri.parse('https://texttospeech.googleapis.com/v1/text:synthesize?key=$apiKey'),
        headers: {'Content-Type': 'application/json'},
        body: jsonEncode({
          'input': {'text': text},
          'voice': {
            'languageCode': langCode,
            'name': voiceName,
          },
          'audioConfig': {
            'audioEncoding': 'MP3',
            'speakingRate': 1.0,
            'pitch': 0.0,
          }
        }),
      );

      // Handle successful response
      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        final audioBase64 = data['audioContent'] as String?;
        
        if (audioBase64 != null) {
          // Save audio to app documents directory
          final directory = await getApplicationDocumentsDirectory();
          final audioFile = File(
            '${directory.path}/zara_${DateTime.now().millisecondsSinceEpoch}.mp3',
          );
          await audioFile.writeAsBytes(base64Decode(audioBase64));
          
          if (kDebugMode) {
            debugPrint('✅ TTS Audio Saved: ${audioFile.path}');
          }
          return audioFile.path;
        }
      }
      
      if (kDebugMode) {
        debugPrint('⚠️ TTS API Error: ${response.statusCode}');
      }
      return null;
      
    } catch (e) {
      if (kDebugMode) {
        debugPrint('⚠️ TTS Error: $e');
      }
      return null;
    }
  }

  // ========== 4. SPEECH-TO-TEXT (GEMINI/GOOGLE CLOUD) ==========
  
  /// Convert speech audio to text using Google Cloud Speech-to-Text API
  /// 
  /// [audioFilePath]: Path to MP3/WAV audio file
  /// [languageCode]: Optional language code (defaults to ApiKeys.languageCode)
  /// 
  /// Returns: Transcribed text, or null on error
  Future<String?> speechToText({
    required String audioFilePath,
    String? languageCode,
  }) async {
    try {
      final apiKey = ApiKeys.gemini;
      
      if (!_isValidKey(apiKey)) {
        if (kDebugMode) debugPrint('⚠️ Gemini API key not configured for STT');
        return null;
      }

      final langCode = languageCode ?? ApiKeys.languageCode;

      // Read audio file
      final audioFile = File(audioFilePath);
      if (!await audioFile.exists()) {
        if (kDebugMode) debugPrint('⚠️ Audio file not found: $audioFilePath');
        return null;
      }

      final audioBytes = await audioFile.readAsBytes();
      final base64Audio = base64Encode(audioBytes);

      // Make request to Google Cloud Speech-to-Text API
      final response = await http.post(
        Uri.parse('https://speech.googleapis.com/v1/speech:recognize?key=$apiKey'),
        headers: {'Content-Type': 'application/json'},
        body: jsonEncode({
          'config': {
            'encoding': 'MP3',
            'sampleRateHertz': 16000,
            'languageCode': langCode,
            'enableAutomaticPunctuation': true,
          },
          'audio': {
            'content': base64Audio,
          }
        }),
      );

      // Handle successful response
      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        final transcript = data['results']?[0]?['alternatives']?[0]?['transcript'] as String?;
        
        if (kDebugMode && transcript != null) {
          debugPrint('🎤 STT Transcript: $transcript');
        }
        return transcript;
      }
      
      if (kDebugMode) {
        debugPrint('⚠️ STT API Error: ${response.statusCode}');
      }
      return null;
      
    } catch (e) {
      if (kDebugMode) {
        debugPrint('⚠️ STT Error: $e');
      }
      return null;
    }
  }

