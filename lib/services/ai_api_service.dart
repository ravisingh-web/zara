/// Centralized AI API service for Z.A.R.A.
/// Routes requests to appropriate AI provider based on task type:
/// - Qwen: Code generation (primary)
/// - Llama: Emotional conversations (Hinglish personality)
/// - Gemini: Voice TTS/STT, search, file analysis
