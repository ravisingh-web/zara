  // ========== Singleton Pattern ==========
  
  static final AiApiService _instance = AiApiService._internal();
  factory AiApiService() => _instance;
  AiApiService._internal();

  // ========== Chat History for Emotional Context ==========
  
  /// Conversation history for Llama emotional chat (last 20 messages)
  final List<Map<String, String>> _chatHistory = [];
  
  /// Maximum history length to prevent memory bloat
  static const int _maxHistoryLength = 20;

  // ========== 1. CODE GENERATION (QWEN API) ==========
  
  /// Generate Flutter/Dart code using Qwen API
  /// 
  /// [filename]: Target file name (e.g., 'main.dart')
  /// [logic]: Description of what the code should do
  /// [features]: Comma-separated list of features to include
  /// [additionalContext]: Optional extra context for the AI
  /// 
  /// Returns: Generated code string or error comment
  Future<String> generateCode({
    required String filename,
    required String logic,
    required String features,
    String? additionalContext,
  }) async {
    try {
      final apiKey = ApiKeys.qwen;
      
      // Validate API key before making request
      if (!_isValidKey(apiKey)) {
        debugPrint('⚠️ Qwen API key not configured');
        return '// ⚠️ Qwen API key not configured\n// Go to Settings → Add API Key';
      }

      // Build comprehensive prompt for code generation
      final prompt = '''
You are Z.A.R.A.'s Code Generation Engine powered by Qwen.

TASK: Generate complete, production-ready Flutter/Dart code

File: $filename
Logic: $logic
Features: $features
${additionalContext != null ? 'Additional Context: $additionalContext' : ''}

REQUIREMENTS:
- Use Flutter 3.41+ latest practices
- Include all necessary imports
- Add detailed comments for key sections
- Follow clean architecture principles
- Use Provider for state management
- Implement holographic UI theme (cyan #00F0FF, dark background #0A0E17)
- Handle errors gracefully with try-catch
- Make it Sir-friendly with personality comments

OUTPUT FORMAT:
- ONLY the code, no explanations or markdown
- No ```dart blocks — just raw code
- Ready to copy-paste into Flutter project

Z.A.R.A. Signature Comment at top:
// Generated by Z.A.R.A. • Qwen Code Engine • Sir, ready hoon! ❤️
''';

      // Make API request to Qwen (DashScope)
      final response = await http.post(
        Uri.parse('https://dashscope.aliyuncs.com/api/v1/services/aigc/text-generation/generation'),
        headers: {
          'Content-Type': 'application/json',
          'Authorization': 'Bearer $apiKey',
        },
        body: jsonEncode({
          'model': 'qwen-turbo',
          'input': {
            'messages': [
              {'role': 'system', 'content': 'You are an expert Flutter developer. Output only code.'},
              {'role': 'user', 'content': prompt}
            ]
          },
          'parameters': {
            'temperature': 0.7,  // Balanced creativity vs consistency
            'max_tokens': 8192,   // Allow long code outputs
          }
        }),
      );

      // Handle successful response
      if (response.statusCode == 200) {
        final data = jsonDecode(response.body);
        String code = data['output']?['text'] ?? '';
        
        // Clean up any markdown code blocks if present
        code = code.replaceAll('```dart', '').replaceAll('```', '').trim();
        
        if (kDebugMode) {
          debugPrint('✅ Qwen Code Generated: ${code.length} chars');
        }
        return code;
      }
      
      // Handle API error
      if (kDebugMode) {
        debugPrint('⚠️ Qwen API Error: ${response.statusCode}');
        debugPrint('Response: ${response.body}');
      }
      return '// ⚠️ Qwen API Error: ${response.statusCode}\n// Please check your API key in Settings';
      
    } catch (e) {
      if (kDebugMode) {
        debugPrint('⚠️ Code Generation Error: $e');
      }
      return '// ⚠️ Error: $e\n// Check internet connection and API key';
    }
  }

