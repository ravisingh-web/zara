  // ========== 6. CODE ANALYSIS (QWEN/GEMINI) ==========
  
  /// Analyze Dart/Flutter code for issues and suggestions
  /// 
  /// [code]: Code string to analyze
  /// 
  /// Returns: CodeAnalysisResult with issues, suggestions, stats
  Future<CodeAnalysisResult> analyzeCode(String code) async {
    try {
      // For now, use basic heuristic analysis
      // Full implementation would call Qwen/Gemini for AI-powered analysis
      
      final issues = <CodeIssue>[];
      final suggestions = <String>[];
      
      // Basic bracket balance check
      final openBrackets = code.split('').where((c) => '({['.contains(c)).length;
      final closeBrackets = code.split('').where((c) => ')}]'.contains(c)).length;
      
      if (openBrackets != closeBrackets) {
        issues.add(CodeIssue(
          type: IssueType.syntaxError,
          message: 'Bracket mismatch: $openBrackets open, $closeBrackets close',
          line: 0,
          column: 0,
          suggestion: 'Check for missing closing brackets',
        ));
        suggestions.add('⚠️ Fix bracket balance');
      }
      
      // Check for common Flutter issues
      if (code.contains('print(') && !code.contains('//')) {
        suggestions.add('💡 Remove print() statements before production');
      }
      
      if (!code.contains('import ') && code.length > 100) {
        suggestions.add('💡 Add necessary imports for used widgets/classes');
      }
      
      // Count classes and functions (basic heuristic)
      final classCount = RegExp(r'\bclass\s+\w+').allMatches(code).length;
      final functionCount = RegExp(r'\b(?:void|Future|String|int|bool|Widget)\s+\w+\s*\(').allMatches(code).length;
      
      final isValid = issues.isEmpty;
      
      return CodeAnalysisResult(
        isValid: isValid,
        issues: issues,
        suggestions: suggestions.isEmpty ? ['✨ Code looks good, Sir!'] : suggestions,
        lineCount: code.split('\n').length,
        characterCount: code.length,
        classCount: {'detected': classCount},
        functionCount: {'detected': functionCount},
      );
      
    } catch (e) {
      if (kDebugMode) {
        debugPrint('⚠️ Code Analysis Error: $e');
      }
      return CodeAnalysisResult(
        isValid: false,
        issues: [CodeIssue(
          type: IssueType.syntaxError,
          message: 'Analysis failed: $e',
          line: 0,
          column: 0,
        )],
        suggestions: ['⚠️ Try simplifying the code or check syntax'],
        lineCount: 0,
        characterCount: 0,
        classCount: {},
        functionCount: {},
      );
    }
  }

  // ========== 7. AUTO-FIX CODE (SIMULATION) ==========
  
  /// Attempt to auto-fix common code issues
  /// 
  /// [code]: Code string to fix
  /// 
  /// Returns: "Fixed" code string (simulation — real fix needs AI)
  Future<String> autoFixCode(String code) async {
    // Basic bracket balancing (naive approach)
    var fixed = code;
    final openBrackets = fixed.split('').where((c) => '({['.contains(c)).length;
    final closeBrackets = fixed.split('').where((c) => ')}]'.contains(c)).length;
    
    // Add missing closing brackets (simplified — assumes parentheses)
    if (openBrackets > closeBrackets) {
      final missing = openBrackets - closeBrackets;
      fixed += ')' * missing;
    }
    
    // Add missing import comment placeholder
    if (!fixed.contains('import ') && fixed.length > 50) {
      fixed = '// TODO: Add necessary imports\n$fixed';
    }
    
    return fixed;
  }

  // ========== UTILITY METHODS ==========
  
  /// Validate API key format (basic heuristic)
  bool _isValidKey(String key) {
    return key.isNotEmpty && 
           key.length > 20 && 
           !key.contains('your_') &&
           !key.contains('paste_') &&
           !key.contains('xxxxx');
  }

  /// Check status of all configured API keys
  Future<Map<String, bool>> checkStatus() async {
    return {
      'gemini': _isValidKey(ApiKeys.gemini),
      'qwen': _isValidKey(ApiKeys.qwen),
      'llama': _isValidKey(ApiKeys.llama),
      'all': ApiKeys.isConfigured,
    };
  }

  /// Get API routing configuration (for other services)
  Map<String, String> get apiRouting {
    return {
      'code': 'qwen',
      'chat': 'llama',
      'voice': 'gemini',
      'search': 'gemini',
      'files': 'gemini',
    };
  }
}

// ========== SUPPORTING MODELS ==========

/// Result of code analysis operation
class CodeAnalysisResult {
  final bool isValid;
  final List<CodeIssue> issues;
  final List<String> suggestions;
  final int lineCount;
  final int characterCount;
  final Map<String, int> classCount;
  final Map<String, int> functionCount;

  const CodeAnalysisResult({
    required this.isValid,
    required this.issues,
    required this.suggestions,
    required this.lineCount,
    required this.characterCount,
    required this.classCount,
    required this.functionCount,
  });
}

/// Represents a single code issue found during analysis
class CodeIssue {
  final IssueType type;
  final String message;
  final int line;
  final int column;
  final String? suggestion;

  const CodeIssue({
    required this.type,
    required this.message,
    required this.line,
    required this.column,
    this.suggestion,
  });
}

/// Categories of code issues
enum IssueType {
  syntaxError,
  unusedImport,
  unusedVariable,
  missingReturn,
  typeMismatch,
  nullSafety,
  performance,
  style,
  security,
}
